- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Configure the cell...
    
    switch ([indexPath row]) {
        case 0:
            cell.textLabel.text = [[[NSString alloc] initWithFormat:@"App version: %@", APP_VERSION] autorelease];
            break;
        case 1:
            cell.textLabel.text = [[[NSString alloc] initWithFormat:@"SDK version: %@", APP_SDK_VERSION] autorelease];
            break;
        case 2:
            cell.textLabel.text = [[[NSString alloc] initWithFormat:@"App original: %@", APP_IOS_VERSION] autorelease];
            break;
            
        default:
            break;
    }
    
    [cell setSelectionStyle:UITableViewCellSelectionStyleNone];
    
    return cell;
}

